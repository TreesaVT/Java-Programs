package basicprogram;

public class IfElseBlock 
{
    public static void main(String[] args) 
    {
		int age=70;
		if(age>60)
		{
			System.out.println("senior citizen");
		}
		if(age>18)
		{
			System.out.println("Eligible to vote");
		}
		else
		{
			System.out.println("Not senior citizen");
		}
	}
}

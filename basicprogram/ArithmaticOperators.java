package basicprogram;

public class ArithmaticOperators 
{
public static void main(String[] args) 
   {
	int a=10;
	int b=20;
	int sum= a+b;
	int sub=a-b;
	int mul=a*b;
	int div=b/a;
	int mod=b%a;
    System.out.println(sum);
    System.out.println(sub);
    System.out.println(mul);
    System.out.println(div);
    System.out.println(mod);
   }
}
